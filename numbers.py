# int and float
num = 42 / 9
print(type(num), num)
num = 42 // 9
print(type(num), num)
num = 42 % 9
print(type(num), num)
num = int(42.9)
print(type(num), num)
num = float(42.9)
print(type(num), num)

tuplVar = (1, 2, 3)
print(type(tuplVar), tuplVar)

listVar = [1,2,3]
listVar.append(5)
listVar.insert(0, 2)
print(type(listVar), listVar)
